package ch02_exfile;

public class CharBooDemo {

	public static void main(String[] args) {
		char gall ='가';//char : 문자 테이터 저장 타입,' -> 16byte
		int a =10;
		boolean cham = (a == 10);// == -> 비교연사자중 같은지 묻는 연산자
		boolean geojit = (a != 10);// != -> 비교연사자중 다른지 묻는 연산자
		System.out.println(gall);
		System.out.println(cham);
		System.out.println(geojit);
	}

}
